	NUM INT NOT NULL AUTO_INCREMENT,
	TITLE VARCHAR(50) NOT NULL,
	AUTHOR VARCHAR(30) NOT NULL,
	COMPANY VARCHAR(50) NOT NULL,
	ISBN VARCHAR(30) NOT NULL,
	COUNT INT NOT NULL DEFAULT 0,
	PRIMARY KEY(NUM)
);

